public class Item {
    public int id;
    public String nome;
    public String tipo;
    public double valor;
    public String descricao;

    public Item(int id, String nome, String tipo, double valor ,String descricao){
        this.id = id;
        this.nome = nome;
        this.tipo = tipo;
        this.valor = valor;
        this.descricao = descricao; 
    }
}
